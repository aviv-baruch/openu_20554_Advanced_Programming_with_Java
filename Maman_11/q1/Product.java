package q1;

public class Product {
    // Instance variables
    private String label;
    private double price;

    // Constructor
    public Product(String label, double price) {
        this.label = label;
        this.price = price;
    }

    // Getter method for label
    public String getLabel() {
        return label;
    }

    // Getter method for price
    public double getPrice() {
        return price;
    }
}
